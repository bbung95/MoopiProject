문의게시판 리스트

SELECT 
    BD.BD_NO
  , U.NICKNAME
  , BD.BD_NAME
  , BD.BD_REGDATE
  , BD.BD_UPDATE
  , BD.BD_PASSWORD
  , COUNT(R.REPLY_CONTENT) REPLYCOUNT
  FROM BOARD BD, USERS U, REPLY R
WHERE 
BD.BD_WRITER = U.USER_ID
AND
BD.BD_NO = R.BD_NO
AND
bd.bd_category = 2
GROUP BY BD.BD_NO, U.NICKNAME, BD.BD_NAME, BD.BD_REGDATE, 
BD.BD_UPDATE, BD.BD_PASSWORD

