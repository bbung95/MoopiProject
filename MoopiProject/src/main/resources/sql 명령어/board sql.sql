SELECT 
   BD.BD_NO
  , U.NICKNAME
  , BD.BD_NAME
  , BD.BD_CONTENT
  , BD.BD_REGDATE
  , BD.BD_UPDATE
FROM BOARD BD, USERS U
WHERE 
BD.BD_WRITER = U.USER_ID
AND
BD_CATEGORY = 1 
AND 
BD_STATE = 1;

