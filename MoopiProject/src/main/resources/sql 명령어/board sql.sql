  B.BD_NO
, B.BD_NAME
, B.BD_CONTENT
, B.BD_REGDATE
, B.BD_UPDATE
, B.BD_WRITER
, R.REPLY_CONTENT
, R.REPLY_REGDATE
, R.REPLY_WRITER_ID
, r.reply_file
FROM
BOARD B
FULL OUTER JOIN REPLY R 
ON
B.BD_NO = R.BD_NO
WHERE 
R.BD_NO = 10004 



